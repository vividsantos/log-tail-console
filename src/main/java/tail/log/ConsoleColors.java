package tail.log;

public class ConsoleColors {
    // Reset
    public static final String RESET = "\033[0m";

    // Cores de texto
    public static final String RED = "\033[0;31m";
    public static final String GREEN = "\033[0;32m";
    public static final String YELLOW = "\033[0;33m";
    public static final String GRAY = "\033[0;90m";
    public static final String BLUE = "\033[0;34m";
    public static final String PURPLE = "\033[0;35m";
    public static final String CYAN = "\033[0;36m";
    public static final String WHITE = "\033[0;37m";

    // Cores brilhantes
    public static final String BRIGHT_RED = "\033[0;91m";
    public static final String BRIGHT_GREEN = "\033[0;92m";
    public static final String BRIGHT_YELLOW = "\033[0;93m";
    public static final String BRIGHT_WHITE = "\033[0;97m";
    public static final String BRIGHT_BLUE = "\033[0;94m";

    // Cores escuras
    public static final String DARK_RED = "\033[0;31m";
    public static final String DARK_GREEN = "\033[0;32m";
    public static final String BROWN = "\033[0;33m";
    public static final String DARK_BLUE = "\033[0;34m";
    public static final String BLACK = "\033[0;30m";

    // High contrast
    public static final String BOLD_RED = "\033[1;31m";        // Vermelho negrito
    public static final String BOLD_YELLOW = "\033[1;33m";     // Amarelo negrito
    public static final String BOLD_CYAN = "\033[1;36m";       // Ciano negrito
    public static final String BOLD_MAGENTA = "\033[1;35m";    // Magenta negrito
    public static final String BOLD_WHITE = "\033[1;37m";

    // Cores de fundo
    public static final String BG_RED = "\033[41m";
    public static final String BG_GREEN = "\033[42m";
    public static final String BG_YELLOW = "\033[43m";

}
